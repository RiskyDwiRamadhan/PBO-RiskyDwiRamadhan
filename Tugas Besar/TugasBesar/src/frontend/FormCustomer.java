    private void tblKariyawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKariyawanMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblKariyawan.getModel();
        int row = tblKariyawan.getSelectedRow();

        Customer kat = new Customer().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        id.setText(model.getValueAt(row, 0).toString());
        txtNamaKar.setText(model.getValueAt(row, 1).toString());
        txtAlamatKar.setText(model.getValueAt(row, 2).toString());
        txtTelepon.setText(model.getValueAt(row, 3).toString());
        txtUser.setText(model.getValueAt(row, 4).toString());
        txtPass.setText(kat.getPass());
    }//GEN-LAST:event_tblKariyawanMouseClicked

