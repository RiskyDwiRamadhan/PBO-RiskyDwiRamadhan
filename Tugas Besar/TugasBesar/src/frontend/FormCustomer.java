    private void kosongkanForm() {        
        txtNamaKar.setText("");
        txtAlamatKar.setText("");
        txtTelepon.setText("");
        txtUser.setText("");
        txtPass.setText("");
    }
    
    private void tampilkanData() {
        String [] kolom ={"ID ", "Nama", "Alamat", "Telepon", "Username"};
        ArrayList<Customer> list = new Customer().getAll();
        Object rowData[] = new Object[5];
        
        if(id.getText().equals("")){
            id.setText("1");
        }
        
        tblKariyawan.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Customer k : list){
            rowData[0] = k.getId();
            rowData[1] = k.getNama();
            rowData[2] = k.getAlamat();
            rowData[3] = k.getTelepon();
            rowData[4] = k.getUser();
            
            ((DefaultTableModel)tblKariyawan.getModel()).addRow(rowData);
            getIdC();
        }
    }
    
    public void cari(String keyword){
        String [] kolom ={"ID ", "Nama", "Alamat", "Telepon", "Username"};
        ArrayList<Customer> list = new Customer().search(keyword);
        Object rowData[] = new Object[5];
        
        tblKariyawan.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Customer k : list){
            rowData[0] = k.getId();
            rowData[1] = k.getNama();
            rowData[2] = k.getAlamat();
            rowData[3] = k.getTelepon();
            rowData[4] = k.getUser();
            ((DefaultTableModel)tblKariyawan.getModel()).addRow(rowData);
        }
    }
    
    public void getIdC(){
        ResultSet rs = DBHelper.selectQuery("select * from user");
        try {
            while (rs.next()) {
                id.setText(String.valueOf(rs.getInt("id")+1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Customer k = new Customer();
        
        k.setId(Integer.parseInt(id.getText()));
        k.setNama(txtNamaKar.getText());
        k.setAlamat(txtAlamatKar.getText());
        k.setTelepon(txtTelepon.getText());
        k.setUser(txtUser.getText());
        k.setPass(txtPass.getText());
        k.setStatus("");

        k.save();
        tampilkanData();
        kosongkanForm();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblKariyawan.getModel();
        int row = tblKariyawan.getSelectedRow();

        Customer kat = new Customer().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        kat.delete();

        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

